public class Rhombus2  
{
   public static void main(String[] args)
   {
      c();
      c();
      code2();
      code1();
      System.out.println("    \"-'-\"-'-\"       ");
      code2();
   }
   public static void c()
   {  
      code1();
      code2();
      System.out.println("    \"-'-\"-'-\"       ");
   }
       
   public static void code1()
   {
      System.out.println("       $$$       ");
      System.out.println("      $   $       ");
      System.out.println("     $     $       ");
   }
   public static void code2()
   {
      System.out.println("     $     $       ");
      System.out.println("      $   $       ");
      System.out.println("       $$$       ");
   }
}
