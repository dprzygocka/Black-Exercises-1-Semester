public class RhombusStart 
{
   public static void main(String[] args)
   {
      m1();
      m2();
      m2();
      m3();
      m1();
      System.out.println("| START |");
      m2();
      m3();
   }
   public static void m1()
   {
      System.out.println("   $$$   ");
      System.out.println("  $   $    ");
      System.out.println(" $     $    "); 
   }
    public static void m2()
   {
      System.out.println(" $     $   ");
      System.out.println("  $   $    ");
      System.out.println("   $$$    "); 
   }
   public static void m3()
   {
      System.out.println("+--------+");
   }
}