public class Rhyme 
{
   public static void main(String[] args)
   {  
      m1();
      m2();
      m1();
      m2();
      m1();
   } 
   public static void m1()
   {
      System.out.println("A sunshiny shower\nWon't last an hour.\n");
      System.out.println("A sunshiny shower\nWon't last an hour.");
   }
   public static void m2()
   {
      System.out.println("Rain before seven;\nClear by eleven.");
   }
}
   
